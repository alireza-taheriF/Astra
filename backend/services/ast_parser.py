def get_parser(file_path: str, source: str) -> BaseCodeParser:
    """Return the appropriate parser for *file_path* based on its extension.

    Raises :class:`ValueError` for unsupported extensions and may propagate
    :class:`SyntaxError` (Python) if *source* cannot be parsed.
    """
    ext = os.path.splitext(file_path)[1].lower()
    language = _EXT_LANGUAGE.get(ext)
    if language is None:
        raise ValueError(f"Unsupported file extension for parsing: {ext!r}")

    if language == "python":
        return PythonParser(source, file_path=file_path)
    return TreeSitterParser(source, language=language, file_path=file_path)
