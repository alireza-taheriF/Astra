# Tree-sitter (cpp / javascript / typescript / tsx)
# ---------------------------------------------------------------------------

# Node types counted as decision points across the tree-sitter grammars. Loop
# and conditional variants are included as equivalents of Python's set.
_TS_DECISION_NODES = frozenset(
    {
        "if_statement",
        "for_statement",
        "for_in_statement",
        "for_range_loop",
        "while_statement",
        "do_statement",
        "catch_clause",
        "case_statement",
        "switch_case",
        "ternary_expression",
        "conditional_expression",
    }
)

# CUDA authoring markers detected lexically for .cu/.cpp sources. These are
# language keywords/operators that only appear when authoring device code.
_CUDA_KERNEL_MARKERS = ("__global__", "__device__", "<<<")


@lru_cache(maxsize=None)
def _load_language(language: str):
    """Load and cache a tree-sitter ``Language`` for the given identifier.

    Imports are performed lazily so that Python-only analysis does not require
    the tree-sitter native grammars to be installed.
    """
    try:
        from tree_sitter import Language
    except ImportError as exc:  # pragma: no cover - environment dependent
        raise ImportError(
            "py-tree-sitter is required for non-Python parsing. "
            "Install it with `pip install tree-sitter`."
        ) from exc

    try:
        if language == "cpp":
            import tree_sitter_cpp as ts_lang
            return Language(ts_lang.language())
        if language == "javascript":
            import tree_sitter_javascript as ts_lang
            return Language(ts_lang.language())
        if language == "typescript":
            import tree_sitter_typescript as ts_lang
            return Language(ts_lang.language_typescript())
        if language == "tsx":
            import tree_sitter_typescript as ts_lang
            return Language(ts_lang.language_tsx())
    except ImportError as exc:  # pragma: no cover - environment dependent
        raise ImportError(
            f"Missing tree-sitter grammar for '{language}'. Install the matching "
            f"`tree-sitter-{language.replace('tsx', 'typescript')}` package."
        ) from exc

    raise ValueError(f"Unsupported tree-sitter language: {language!r}")


class TreeSitterParser(BaseCodeParser):
    """py-tree-sitter parser for C/C++/CUDA, JavaScript, and TypeScript."""

    def __init__(self, source: str, language: str, file_path: str = "") -> None:
        super().__init__(source, file_path)
        self.language = language
        from tree_sitter import Parser  # local import: optional dependency

        self._lang = _load_language(language)
        self._parser = Parser(self._lang)
        self._source_bytes = self.source.encode("utf-8")
        self.tree = self._parser.parse(self._source_bytes)
        self._usage_cache: dict[str, dict] | None = None

    # -- query helper -------------------------------------------------------
    def _query_captures(self, query_str: str) -> dict[str, list]:
        from tree_sitter import Query, QueryCursor

        query = Query(self._lang, query_str)
        cursor = QueryCursor(query)
        return cursor.captures(self.tree.root_node)

    def _node_text(self, node) -> str:
        return self._source_bytes[node.start_byte : node.end_byte].decode(
            "utf-8", errors="replace"
        )

    # -- complexity & depth -------------------------------------------------
    def cyclomatic_complexity(self) -> int:
        complexity = 1
        root = self.tree.root_node
        stack = [root]
        while stack:
            node = stack.pop()
            if node.type in _TS_DECISION_NODES:
                complexity += 1
            elif node.type == "binary_expression":
                op = node.child_by_field_name("operator")
                if op is not None and self._node_text(op) in ("&&", "||"):
                    complexity += 1
            stack.extend(node.children)
        return complexity

    def ast_depth(self) -> int:
        def depth(node) -> int:
            # Consider only named children so that punctuation tokens (braces,
            # semicolons) do not inflate the measured nesting depth.
            named = [c for c in node.children if c.is_named]
            if not named:
                return 1
            return 1 + max(depth(child) for child in named)

        return depth(self.tree.root_node)

    # -- library usage ------------------------------------------------------
    def _analyze_usage(self) -> dict[str, dict]:
        if self._usage_cache is not None:
            return self._usage_cache

        bound_names: dict[str, str] = {}   # local identifier -> library
        import_ranges: list[tuple[int, int]] = []

        self._collect_imports(bound_names, import_ranges)

        usage: dict[str, dict] = {
            lib: {"call_sites": 0, "kernel_authoring": False}
            for lib in set(bound_names.values())
        }

        # Count identifier references that fall outside import statements.
        if bound_names:
            captures = self._query_captures("(identifier) @id")
            for node in captures.get("id", []):
                name = self._node_text(node)
                lib = bound_names.get(name)
                if lib is None:
                    continue
                if self._within_ranges(node.start_byte, import_ranges):
                    continue
                usage[lib]["call_sites"] += 1

        # CUDA kernel authoring (device code) is detected lexically for C++.
        if self.language == "cpp" and any(
            marker in self.source for marker in _CUDA_KERNEL_MARKERS
        ):
            usage.setdefault("cuda", {"call_sites": 0, "kernel_authoring": False})
            usage["cuda"]["kernel_authoring"] = True

        self._usage_cache = usage
        return usage

    def _collect_imports(
        self,
        bound_names: dict[str, str],
        import_ranges: list[tuple[int, int]],
    ) -> None:
        # ES module imports: `import express from 'express'`,
        # `import * as ex from 'express'`, `import { Router } from 'express'`.
        try:
            import_caps = self._query_captures(
                "(import_statement) @imp"
            )
        except Exception:
            import_caps = {}
        for node in import_caps.get("imp", []):
            source_node = node.child_by_field_name("source")
            module = None
            if source_node is not None:
                module = self._string_literal_value(source_node)
            if module is None:
                # Fall back: last string child.
                strings = [c for c in node.children if c.type == "string"]
                if strings:
                    module = self._string_literal_value(strings[-1])
            if not module:
                continue
            lib = _resolve_lib(module.split("/")[0])
            if lib is None:
                continue
            import_ranges.append((node.start_byte, node.end_byte))
            for ident in self._descendant_identifiers(node):
                if self._node_text(ident) != module:
                    bound_names[self._node_text(ident)] = lib
            # Also bind the module base name itself (default import commonly
            # shares the package name, e.g. `import express from 'express'`).
            bound_names.setdefault(module.split("/")[0], lib)

        # CommonJS: `const express = require('express')`.
        try:
            require_caps = self._query_captures(
                """
                (call_expression
                  function: (identifier) @fn
                  arguments: (arguments (string) @arg))
                """
            )
        except Exception:
            require_caps = {}
        fns = require_caps.get("fn", [])
        args = require_caps.get("arg", [])
        for fn_node, arg_node in zip(fns, args):
            if self._node_text(fn_node) != "require":
                continue
            module = self._string_literal_value(arg_node)
            if not module:
                continue
            lib = _resolve_lib(module.split("/")[0])
            if lib is None:
                continue
            # Bind the enclosing declarator identifier, if present.
            decl = self._enclosing_declarator_name(arg_node)
            if decl:
                bound_names[decl] = lib
                import_ranges.append(
                    self._declaration_range(arg_node)
                )

        # C/C++ #include lines are not library imports in the registry sense;
        # CUDA authoring is handled by the lexical marker scan instead.

    def _string_literal_value(self, node) -> str | None:
        text = self._node_text(node)
        # Strip surrounding quotes (single, double, or backtick).
        if len(text) >= 2 and text[0] in "\"'`" and text[-1] == text[0]:
            return text[1:-1]
        # `string` nodes sometimes wrap a `string_fragment` child.
        for child in node.children:
            if child.type in ("string_fragment", "string_content"):
                return self._node_text(child)
        return None

    def _descendant_identifiers(self, node) -> list:
        found = []
        stack = list(node.children)
        while stack:
            n = stack.pop()
            if n.type == "identifier":
                found.append(n)
            stack.extend(n.children)
        return found

    def _enclosing_declarator_name(self, node) -> str | None:
        cur = node.parent
        while cur is not None:
            if cur.type == "variable_declarator":
                name_node = cur.child_by_field_name("name")
                if name_node is not None and name_node.type == "identifier":
                    return self._node_text(name_node)
            cur = cur.parent
        return None

    def _declaration_range(self, node) -> tuple[int, int]:
        cur = node.parent
        while cur is not None:
            if cur.type in (
                "variable_declaration",
                "lexical_declaration",
                "expression_statement",
            ):
                return (cur.start_byte, cur.end_byte)
            cur = cur.parent
        return (node.start_byte, node.end_byte)

    @staticmethod
    def _within_ranges(byte_pos: int, ranges: list[tuple[int, int]]) -> bool:
        return any(start <= byte_pos < end for start, end in ranges)

    def library_usage(self) -> dict[str, dict]:
        return self._analyze_usage()

    def detected_libraries(self) -> dict[str, float]:
        return {
            lib: float(facts["call_sites"])
            for lib, facts in self._analyze_usage().items()
        }

    # -- boilerplate --------------------------------------------------------
    def is_boilerplate(self, commit_context: str = "") -> bool:
        if not self.source.strip():
            return True
        if self._has_generated_marker():
            return True

        # A parse dominated by ERROR nodes is not meaningfully analyzable; treat
        # it as non-boilerplate so broken files are not silently up-weighted.
        complexity = self.cyclomatic_complexity()
        significant = self._significant_line_count()

        # Structural triviality for curly-brace languages: only imports/exports
        # and top-level declarations with no control flow.
        trivial = self._is_structurally_trivial()
        if trivial and complexity <= 1:
            return True

        if self._commit_suggests_boilerplate(commit_context) and complexity <= 2:
            return True

        # Extremely small files with no branching (e.g. a re-export barrel).
        if complexity <= 1 and significant <= 3:
            return True

        return False

    def _is_structurally_trivial(self) -> bool:
        # Top-level children that are purely imports/exports/comments count as
        # non-logic. Any control-flow anywhere disqualifies triviality.
        root = self.tree.root_node
        control_present = False
        stack = [root]
        while stack:
            node = stack.pop()
            if node.type in _TS_DECISION_NODES or node.type == "binary_expression":
                if node.type == "binary_expression":
                    op = node.child_by_field_name("operator")
                    if op is not None and self._node_text(op) in ("&&", "||"):
                        control_present = True
                else:
                    control_present = True
            stack.extend(node.children)
        if control_present:
            return False

        non_trivial_top = 0
        for child in root.children:
            if not child.is_named:
                continue
            if child.type in (
                "import_statement",
                "comment",
                "preproc_include",
                "export_statement",
            ):
                continue
            non_trivial_top += 1
        return non_trivial_top == 0


